#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const packageRoot = process.env.CODEAGENT_FEATURE_PACKAGES_ROOT
  ? path.resolve(process.env.CODEAGENT_FEATURE_PACKAGES_ROOT)
  : path.resolve(repoRoot, '..', 'code-agent-packages');
const outDir = path.join(repoRoot, 'src', 'features', 'package-catalog');
const outFile = path.join(outDir, 'generated.ts');

function readJson(filePath) {
  return JSON.parse(readFileSync(filePath, 'utf8'));
}

function discoverPackageManifests() {
  if (!existsSync(packageRoot)) {
    return [];
  }

  return readdirSync(packageRoot, { withFileTypes: true })
    .filter(entry => entry.isDirectory())
    .map(entry => path.join(packageRoot, entry.name, 'manifest.json'))
    .filter(manifestPath => existsSync(manifestPath))
    .map(manifestPath => readJson(manifestPath))
    .sort((left, right) => String(left.id).localeCompare(String(right.id)));
}

const manifests = discoverPackageManifests();
mkdirSync(outDir, { recursive: true });

const source = [
  "import type { FeaturePackageManifest } from '@codeagent/feature-package-sdk';",
  '',
  '// Generated by scripts/generate-feature-package-catalog.mjs. Do not edit by hand.',
  'export const EXTERNAL_FEATURE_PACKAGE_CATALOG_MANIFESTS: FeaturePackageManifest[] = ',
  `${JSON.stringify(manifests, null, 2)} as FeaturePackageManifest[];`,
  '',
].join('\n');

writeFileSync(outFile, source);
console.log(`Generated ${path.relative(repoRoot, outFile)} with ${manifests.length} package manifest(s).`);
